#import <Foundation/Foundation.h>
#import "InputCollector.h"
#import "Player.h"

int main(int argc, const char * argv[]) {
    @autoreleasepool {
        
        InputCollector *myInputCollector = [[InputCollector alloc] init];
        
        
        // insert code here...
        NSLog(@"Hello, World!");
        BOOL gameOn = true;
        Player *p1 = [[Player alloc] init];
        
        
        
        NSString *userInput;
        
        
        
        while (gameOn) {
            userInput = [myInputCollector inputForPrompt:@"Enter command: "];
            
            if ([userInput isEqualToString:@"h"]) {
                [myInputCollector printHistory];
            }
            else if ([userInput isEqualToString:@"r"]) {
                [p1 roll];
            }
        }
        
        
        
        
        
        
        
        
    }
    return 0;
}

































































